void solve() {
    int x1, y1, x2, y2, x3, y3, x4, y4;
    cin >> x1 >> y1 >> x2 >> y2 >> x3 >> y3 >> x4 >> y4;
    int a1 = y1-y2, b1 = x2-x1, c1 = -(a1 * x1 + b1 * y1);
    int a2 = y3-y4, b2 = x4-x3, c2 = -(a2 * x3 + b2 * y3);
    if (a1 * b2 == a2 * b1) {
        if (b1 * c2 == b2 * c1) {
            cout << "coincident\n";
        } else {
            cout << "parallel\n";
        }
    } else {
        float fx = (b1*c2-b2*c1)/(b2*a1-b1*a2);
        float fy = (a1*c2-a2*c1)/(a2*b1-a1*b2);
        cout << fx << " " << fy << "\n";
    }
}

